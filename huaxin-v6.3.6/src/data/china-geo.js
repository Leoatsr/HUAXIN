// 把坐标数组转成 GeoJSON Polygon
export function toGeoPolygon(coords) {
  return {
    type: 'Feature',
    geometry: {
      type: 'Polygon',
      coordinates: [coords]
    }
  };
}

// 把坐标数组转成 GeoJSON LineString（河流用）
export function toGeoLine(coords) {
  return {
    type: 'Feature',
    geometry: {
      type: 'LineString',
      coordinates: coords
    }
  };
}
