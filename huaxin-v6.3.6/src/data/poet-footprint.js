/* ═══ 按朝代 + 生年 排序导出 POETS ═══ */
function _birthYear(p) {
  // 从 lifespan 解析生年 · "约 626 - 684" → 626
  const m = p.lifespan.match(/(\d{3,4})/);
  return m ? parseInt(m[1]) : 9999;
}

function _dynastyOrder(p) {
  if (p.dynasty === '宋') return 5;
  const y = _birthYear(p);
  // 学界常用分期 · 按生年大致：
  //   初唐 ≤ 680 · 盛唐 681-750 · 中唐 751-810 · 晚唐 ≥ 811
  if (y <= 680) return 1;
  if (y <= 750) return 2;
  if (y <= 810) return 3;
  return 4;
}

export const POETS = [..._POETS_RAW].sort((a, b) => {
  const da = _dynastyOrder(a);
  const db = _dynastyOrder(b);
  if (da !== db) return da - db;
  return _birthYear(a) - _birthYear(b);
});

export const POETS_BY_DYNASTY = {
  '初唐': POETS.filter(p => _dynastyOrder(p) === 1),
  '盛唐': POETS.filter(p => _dynastyOrder(p) === 2),
  '中唐': POETS.filter(p => _dynastyOrder(p) === 3),
  '晚唐': POETS.filter(p => _dynastyOrder(p) === 4),
  '宋':   POETS.filter(p => _dynastyOrder(p) === 5)
};

